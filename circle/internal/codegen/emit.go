func (e *emitter) line(s string) {
	e.buf.WriteString(s)
	e.buf.WriteString("\n")
}

// goType 把 IRType 映射成 Go 类型
func goType(t ir.IRType) string {
	switch t.Kind {
	case ir.TypeStr:
		return "string"
	case ir.TypeNum:
		return "int"
	case ir.TypeBool:
		return "bool"
	case ir.TypeByte:
		return "byte"
	case ir.TypeAny:
		return "interface{}"
	}
	return "interface{}"
}

// defaultValue 给 Go 类型一个默认值
func defaultValue(t ir.IRType) string {
	switch t.Kind {
	case ir.TypeStr:
		return `""`
	case ir.TypeNum:
		return "0"
	case ir.TypeBool:
		return "false"
	case ir.TypeByte:
		return "0"
	}
	return "nil"
}

// lowerFirst 把 "hello" 变成 "hello"（保留，备用）
func lowerFirst(s string) string {
	if s == "" {
		return s
	}
	return strings.ToLower(s[:1]) + s[1:]
}

// outputNames 拍平 BlockOutput 列表
func outputNames(outs []ir.BlockOutput) []string {
	out := make([]string, len(outs))
	for i, o := range outs {
		out[i] = o.Name
	}
	return out
}

// hasAnyUsedBlock 节点是否有任何 USED block
func hasAnyUsedBlock(n *ir.IRNode) bool {
	for _, b := range n.Blocks {
		if b.IsUsed {
			return true
		}
	}
	return false
}

// findEdge 在所有包里找 edge
func findEdge(prog *ir.IRProgram, key ir.EdgeKey) *ir.IREdge {
	for _, pkg := range prog.Packages {
		if e, ok := pkg.Edges[key]; ok {
			return e
		}
	}
	return nil
}

// collectCallerMethods 扫所有节点，收集 caller → callee 的 SubEdge 信息
//
// 结果：callerMethods[callee_type] = [{caller_name, edge_name}, ...]
//
// 例：hello 有 SubEdge `h <add_str> w`，w 的 type 是 world
// → callerMethods["world"] = [{CallerName:"hello", EdgeName:"add_str"}]
//
// 调用时机：run() 里 emit 之前
func (e *emitter) collectCallerMethods() {
	e.callerMethods = map[string][]callerMethodInfo{}
	for _, callerNode := range e.nodes {
		for _, se := range callerNode.SubEdges {
			if se.EdgeName == "" || se.DstInstance == "" {
				continue
			}
			// 找 DstInstance 对应的 type
			var calleeType string
			for _, si := range callerNode.SubInstances {
				if si.InstanceName == se.DstInstance {
					calleeType = stripPkg(si.TypeName)
					break
				}
			}
			if calleeType == "" {
				continue
			}
			e.callerMethods[calleeType] = append(e.callerMethods[calleeType], callerMethodInfo{
				CallerName: callerNode.Name,
				EdgeName:   se.EdgeName,
			})
		}
	}
}

// resolveFlowTargetPortsForEmit 把 flow 目标解析成 input port 名列表
// （与 ir.ResolveFlowTargetPorts 一样，但导出的本包版本）
func resolveFlowTargetPortsForEmit(dstNode *ir.IRNode, attrName string) []string {
	if len(dstNode.Inputs) == 0 {
		return nil
	}
	for _, in := range dstNode.Inputs {
		if in.Name == attrName {
			return []string{in.Name}
		}
	}
	return []string{dstNode.Inputs[0].Name}
}
